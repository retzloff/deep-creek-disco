	<h2>Chaos at the Colosseum <br /> (In Space)!!</h2>
	<p>
		A strange colosseum floats in the astral sea. Warriors gather from every plane, with reasons to
		fight as varied as the fighters themselves. Some seek honor, some money, some bloodshed, and
		some simply seek to survive.
	</p>
	You are one such warrior and you find yourself on the sands of the arena, your fate in the hands of
	your skill and the crowd…or is it? Who is the strange announcer that goes only by The Emperor, what
	are those strange sounds you sometimes hear behind the walls, why does the ground sometimes rumble
	beneath your feet? The answer to these mysteries and more await you in the Colosseum!
